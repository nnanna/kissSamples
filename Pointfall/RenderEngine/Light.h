#ifndef LIGHT_H
#define LIGHT_H

#include <Maths/ks_Maths.h>

struct Light
{
	ks::vec3 position;
	ks::vec3 color;

	static inline Light create( const ks::vec3& pPos, const ks::vec3& pCol )
	{
		Light l	= { pPos, pCol };
		return l;
	}

};

#endif